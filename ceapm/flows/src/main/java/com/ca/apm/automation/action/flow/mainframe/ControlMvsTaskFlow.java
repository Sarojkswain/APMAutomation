package com.ca.apm.automation.action.flow.mainframe;

import com.ca.apm.automation.action.core.IAutomationFlow;
import com.ca.apm.automation.action.core.annotations.Flow;
import com.ca.apm.automation.action.core.annotations.FlowContext;
import com.ca.apm.automation.utils.mainframe.MvsTask;

/**
 * Flow that gives the ability to control the state of a started task on a z/OS system.
 */
@Flow
public class ControlMvsTaskFlow implements IAutomationFlow {
    @FlowContext
    private ControlMvsTaskFlowContext context;

    /**
     * Executes the flow - starts, stops or checks the configured task.
     */
    @Override
    public void run() throws Exception {
        MvsTask.execute(context);
    }
}
